# Para rodar o programa, coloque o código no arquivo 
# `ola-mundo.go` e use `go run`.
$ go run ola-mundo.go
Olá mundo

# Às vezes, queremos transformar nosso código
# fonte em um programa executável. Podemos fazer isso 
# usando `go build`.
$ go build ola-mundo.go
$ ls
ola-mundo   ola-mundo.go

# Agora, nós podemos executar o programa diretamente.
$ ./ola-mundo
Olá mundo

